<h2>Gaya Desain Web Modern</h2>
<p>
    Panduan singkat untuk memahami gaya visual populer agar desain selaras dengan
    branding, UX, dan performa.
</p>

<h3>4. Neo-Brutalism</h3>
<p>
    Kebangkitan modern brutalism tradisional — berani, mentah, tanpa permintaan
    maaf.
</p>
<p>
    Neo-brutalism mengambil elemen dari arsitektur brutal dan mengadaptasinya ke
    web design dengan sentuhan modern, menciptakan statement yang kuat dan
    memorable.
</p>
<p><strong>Fitur Utama</strong></p>
<ul>
    <li>Border tegas dan tebal</li>
    <li>Kontras warna tinggi</li>
    <li>Gradien minimal atau transisi keras</li>
    <li>Sengaja terasa “tidak nyaman”</li>
    <li>Tipografi bold dan impactful</li>
</ul>
<p><strong>Dimana digunakan</strong></p>
<ul>
    <li>Portfolio designer</li>
    <li>Website agensi</li>
    <li>Brand kreatif: fashion, art, music</li>
    <li>Proyek eksperimental</li>
</ul>
<p><strong>Pro tips</strong></p>
<ul>
    <li>Jaga navigasi tetap jelas meski desain bold</li>
    <li>Seimbangkan brutalism dengan UX yang baik</li>
    <li>Uji ke target audience karena gaya ini polarizing</li>
</ul>

<h3>5. Minimalist UI</h3>
<p>Desain bersih dan tenang — filosofi “less is more”.</p>
<p>
    Minimalist UI menghapus elemen non-esensial untuk fokus pada konten dan
    efektivitas.
</p>
<p><strong>Fitur Utama</strong></p>
<ul>
    <li>Banyak white space</li>
    <li>Tipografi tipis dan rapi</li>
    <li>Distraksi visual minimal</li>
    <li>Performa cepat dan aksesibel</li>
    <li>Palet warna terbatas</li>
</ul>
<p><strong>Dimana digunakan</strong></p>
<ul>
    <li>Aplikasi produktivitas</li>
    <li>Aplikasi perbankan</li>
    <li>SaaS modern</li>
    <li>Blog dan publication</li>
</ul>
<p><strong>Prinsip desain</strong></p>
<ul>
    <li>Content is king</li>
    <li>Function over form</li>
    <li>Setiap elemen harus punya alasan</li>
    <li>Konsistensi adalah kunci</li>
</ul>

<h3>6. Frost UI (Soft Glass)</h3>
<p>Versi glassmorphism yang lebih ringan, halus, dan subtle.</p>
<p>
    Frost UI mempertahankan efek kaca namun lebih understated, memberi kesan
    premium tanpa berlebihan.
</p>
<p><strong>Fitur Utama</strong></p>
<ul>
    <li>Blur rendah dan halus</li>
    <li>Transparansi lembut</li>
    <li>Palet warna muted</li>
    <li>Tampilan refined dan elegan</li>
</ul>
<p><strong>Dimana digunakan</strong></p>
<ul>
    <li>Mobile UI</li>
    <li>Dashboard premium</li>
    <li>Aplikasi luxury</li>
    <li>Modern web apps</li>
</ul>

<h3>Mengapa Frontend Developer Harus Mengetahui Gaya-Gaya Ini?</h3>
<p><strong>User Experience</strong></p>
<p>
    Gaya yang tepat meningkatkan keterbacaan, usability, dan fokus pengguna.
    Pilihan desain yang selaras mengurangi cognitive load dan dapat meningkatkan
    conversion rate.
</p>
<p><strong>Brand Identity</strong></p>
<p>
    Setiap gaya menyampaikan emosi berbeda: glassmorphism futuristik, neo-brutalism
    berani, minimalist profesional. Ini memperkuat positioning brand.
</p>
<p><strong>Performance</strong></p>
<p>
    Efek visual berat bisa menurunkan FPS. Developer perlu memahami trade-off
    antara estetika dan performa.
</p>
<p><strong>Component Design</strong></p>
<p>
    Pemahaman gaya membantu membangun komponen yang konsisten, scalable, dan
    maintainable dalam design system.
</p>
